function handleGemSpaceMessage(senderMessage) {
    const msg = senderMessage.trim();

    if (msg === 'الالعاب' || msg === 'قائمة الألعاب') {
        return gamesList;
    }
    
    if (msg === 'فلوسي') {
        return '💰 رصيدك الحالي هو: 1,000,000 دينار.';
    }
    
    if (msg === 'عواصم') {
        return '🌍 لعبة العواصم: ما هي عاصمة العراق؟ (اكتب إجابتك بالرد)';
    }

    if (msg === 'سمايلات') {
        return '😀 خمن المعنى من السمايلات التالية: 🍎🐍';
    }

    if (msg === 'بات') {
        return '🥊 لعبة البات: لقد دخلت الحلبة! من تريد أن تضرب؟ (اكتب اسم الشخص بالرد)';
    }

    if (msg === 'مليون' || msg === 'من سيربح المليون') {
        return '🏆 **من سيربح المليون**\nالسؤال الأول:\nما هي عاصمة أستراليا؟\n\nأ) سيدني\nب) ملبورن\nج) كانبرا\nد) بريزبان\n\n(اكتب الحرف أو الإجابة بالرد)';
    }

    // أوامر البنك مع الأوقات الجديدة
    if (msg === 'راتب') {
        return '💵 لقد استلمت راتبك بنجاح! (يمكنك استلام راتب جديد بعد 10 دقائق).';
    }

    if (msg === 'كنز') {
        return '💎 لقد بحثت في الكنز ووجدت مكافأة مالية! (يمكنك فتح كنز جديد بعد 10 دقائق).';
    }

    if (msg.startsWith('استثمار')) {
        return '📈 تم تسجيل استثمارك بنجاح! انتظر النتائج (يمكنك الاستثمار مرة أخرى بعد 20 دقيقة).';
    }

    if (msg.startsWith('حظ')) {
        return '🎲 لفت عجلة الحظ! (يمكنك تجربة الحظ مرة أخرى بعد 20 دقيقة).';
    }

    if (msg.startsWith('يوت ')) {
        const query = msg.replace('يوت ', '').trim();
        const encodedQuery = encodeURIComponent(query);
        return `🎵 جاري البحث عن الأغنية: "${query}"...\n🔗 رابط يوتيوب: https://www.youtube.com/results?search_query=${encodedQuery}`;
    }

    return null;
}

